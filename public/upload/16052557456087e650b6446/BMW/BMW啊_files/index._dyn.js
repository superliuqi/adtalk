  /** Utility method for padding a string with zeroes, e.g. pad("X", 4) => "000X" */
  function pad(str, len) {
    if (typeof str === "number") {
      str = "" + str;
    }
    while (str.length < len) {
      str = "0" + str;
    }
    return str;
  }
  
