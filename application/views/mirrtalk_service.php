<p class=MsoNormal><span lang=EN-US style='font-family:宋体'>1.1 </span><span
style='font-family:宋体'>不匹配个人资料的位置信息、速度信息和加速度信息、里程轨迹以及海拔信息，由终端自动上传并匿名保存。这些数据将通过与其他成员上传数据以及多种其他数据源混合，并经过社区系统计算处理，生成路况预报、交通压力图等数据产品。</span></p>

<p class=MsoNormal><span lang=EN-US style='font-family:宋体'>1.2 </span><span
style='font-family:宋体'>不匹配真实姓名的社区分享由成员主动发起，并由系统自动记载，用于帮助其他交通参与者或与其他成员进行弹性社交互动。</span></p>

<p class=MsoNormal><span lang=EN-US style='font-family:宋体'>1.3 </span><b><span
