<p class=MsoNormal><span lang=EN-US style='font-family:宋体'>2.1 </span><span
style='font-family:宋体'>个人资料：由成员主动填写，仅仅用于为本人服务。仅在成员主动请求或订阅系统服务时，才会将个人资料与位置信息等数据贡献进行匹配。</span></p>

<p class=MsoNormal><span lang=EN-US style='font-family:宋体'>2.2 </span><span
style='font-family:宋体'>急救信息：由成员主动填写，仅在成员需要医疗救助时才可向法定救助机构披露。</span></p>

